"use strict"

/*数组展开：扩展运算符*/
/*语法：...+数组；
 *例：...[1,2,3]会展开为1,2,3; 
 * */
function push(array,...items){
	console.log(items);
	array.push(...items);
}
let a=[];
push(a,1,2,3,4,56);

/*例2：克隆数组:浅拷贝*/
let a1=[1,2,3];
let a2=[...a1];
//通过解构拷贝 :[...a2]=a1;

/*例3：合并数组：浅拷贝*/
let a1=[1,2],a2=[3,4];
let a3=[...a1,...a2];

/*例4：字符串转数组*/
[...'hello']//['h','e','l','l','o'];

//任何定义了遍历器（Iterator）接口的对象，都可以用扩展运算符转为真正的数组。
